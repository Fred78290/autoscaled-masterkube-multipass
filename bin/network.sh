function cidr_to_netmask() {
	local CIDR=$1

	value=$(( 0xFFFFFFFF ^ ((1 << (32 - ${CIDR})) - 1) ))
	echo "$(( (value >> 24) & 0xff )).$(( (value >> 16) & 0xff )).$(( (value >> 8) & 0xff )).$(( value & 0xff ))"
}

