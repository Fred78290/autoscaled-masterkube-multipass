#===========================================================================================================================================
function ipv4() {
	local INF=$1
	local LOCAL_IPADDR=

	if [ "${OSDISTRO}" == "Darwin" ]; then
		read -a LOCAL_IPADDR <<< "$(ifconfig ${INF} | grep -m 1 "inet\s" | sed -n 1p)"
	else
		read -a LOCAL_IPADDR <<< "$(ip addr show ${INF} | grep -m 1 "inet\s" | tr '/' ' ')"
	fi

	echo -n "${LOCAL_IPADDR[1]}"
}
