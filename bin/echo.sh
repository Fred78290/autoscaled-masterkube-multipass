#!/bin/bash

#===========================================================================================================================================
#
#===========================================================================================================================================
function wait_jobs_finish() {
	wait $(jobs -p)
}

#===========================================================================================================================================
#
#===========================================================================================================================================
function echo_blue_dot() {
	echo -n -e "\x1B[90m\x1B[39m\x1B[1m\x1B[34m.\x1B[0m\x1B[39m"
}

#===========================================================================================================================================
#
#===========================================================================================================================================
function echo_blue_dot_title() {
	# echo message in blue and bold
	echo -n -e "\x1B[90m= $(date '+%Y-%m-%d %T') \x1B[39m\x1B[1m\x1B[34m$@\x1B[0m\x1B[39m"
}

#===========================================================================================================================================
#
#===========================================================================================================================================
function echo_blue_bold() {
	# echo message in blue and bold
	echo -e "\x1B[90m= $(date '+%Y-%m-%d %T') \x1B[39m\x1B[1m\x1B[34m$@\x1B[0m\x1B[39m"
}

#===========================================================================================================================================
#
#===========================================================================================================================================
function echo_title() {
	# echo message in blue and bold
	echo
	echo_line
	echo_blue_bold "$@"
	echo_line
}

#===========================================================================================================================================
#
#===========================================================================================================================================
function echo_grey() {
	# echo message in light grey
	echo -e "\x1B[90m$@\x1B[39m"
}

#===========================================================================================================================================
#
#===========================================================================================================================================
function echo_red() {
	# echo message in red
	echo -e "\x1B[31m$@\x1B[39m"
}

#===========================================================================================================================================
#
#===========================================================================================================================================
function echo_red_bold() {
	# echo message in blue and bold
	echo -e "\x1B[90m= $(date '+%Y-%m-%d %T') \x1B[31m\x1B[1m\x1B[31m$@\x1B[0m\x1B[39m"
}

#===========================================================================================================================================
#
#===========================================================================================================================================
function echo_separator() {
	echo_line
	echo
	echo
}

#===========================================================================================================================================
#
#===========================================================================================================================================
function echo_line() {
	echo_grey "============================================================================================================================="
}

