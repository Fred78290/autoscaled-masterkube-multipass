function delete_vm_by_name() {
    local VMNAME=$1

    if [ "$(multipass info ${VMNAME} 2>/dev/null)" ]; then
        echo_blue_bold "Delete VM: $VMNAME"
        multipass delete $VMNAME -p
	fi

    delete_host "${VMNAME}"
}

