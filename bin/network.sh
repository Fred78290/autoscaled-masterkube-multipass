function nextip()
{
	local IP=$1

	if [ "${IP}" == "DHCP" ] || [ "${IP}" == "NONE" ]; then
		echo "${IP}"
	else
		local IP_HEX=$(printf '%.2X%.2X%.2X%.2X\n' `echo ${IP} | tr '.' ' '`)
		local NEXT_IP_HEX=$(printf %.8X `echo $(( 0x${IP_HEX} + 1 ))`)
		local NEXT_IP=$(printf '%d.%d.%d.%d\n' `echo ${NEXT_IP_HEX} | sed -r 's/(..)/0x\1\ /g'`)

		echo "${NEXT_IP}"
	fi
}

